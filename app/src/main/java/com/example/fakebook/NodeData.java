package com.example.fakebook;

public class NodeData {
    String tagName;
    String innerText;

    public NodeData(String tagName, String innerText){
        this.tagName = tagName;
        this.innerText = innerText;
    }

    public String getTagName() {
        return tagName;
    }

    public String getInnerText() {
        return tagName;
    }
}
